/*请将本函数补充完整，并进行测试*/
void partion(sequence_list *L)
{
	int tmp;
	int i = 0, j = L->size - 1;
	while(i < j){
		while (L->a[i] % 2 != 0 && i<j) {
			i++;
		}
		while (L->a[j] % 2 == 0 && i<j) {
			j--;
		}
		tmp = L->a[i];
		L->a[i] = L->a[j];
		L->a[j] = tmp;
	}
	return 0;
}
int main()
{
  sequence_list L;
  /*同上，我的电脑环境不支持这个函数
  inputfromfile(&L,"3.txt");*/
  input(&L);
  print(&L);  						/*输出表L*/
  partion(&L);
  print(&L);  						/*输出新表*/
  return 0;
}
